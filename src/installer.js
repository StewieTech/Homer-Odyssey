function initializeTarget({ targetRoot, profile: profileValue }) {
  const resolvedTarget = path.resolve(targetRoot || '.');
  if (!fs.existsSync(resolvedTarget) || !fs.statSync(resolvedTarget).isDirectory()) throw new HomerError(`Target root does not exist: ${resolvedTarget}`, EXIT.INVALID_CONTRACT);
  if (!profileValue) throw new HomerError('homer install --init-target requires --profile <target-profile-path>', EXIT.USAGE);
  const profilePath = path.isAbsolute(profileValue) ? path.resolve(profileValue) : path.resolve(resolvedTarget, profileValue);
  if (!isInside(resolvedTarget, profilePath)) throw new HomerError('Install profile must be owned by the target repository', EXIT.INVALID_CONTRACT);
  const adrPath = path.join(resolvedTarget, '.homer', 'adr', '0001-homer-target-sanitization.md');
  const existing = [profilePath, adrPath].filter((item) => fs.existsSync(item));
  if (existing.length) throw new HomerError('Target initialization refuses to overwrite existing policy or ADR', EXIT.CUSTOMIZATION_CONFLICT, existing);
  const profileContent = `${stableJson(safeDenyProfile(), 2)}\n`;
  const adrContent = `# ADR 0001: Homer target sanitization policy\n\nStatus: Policy review required\n\n## Context\n\nHomer can project canonical character packages into native Codex skills. The target profile is executable policy; this ADR records the target-owned rationale.\n\n## Decision required\n\nReview repository identity, base branch, package allowlists, exact native skill paths, capabilities, substitutions, overlays, protected paths, validation commands, and minimum versions. Keep generated output read-only and preserve local rules as higher precedence.\n\n## Consequences\n\nNo installation is permitted until the safe-deny profile is reviewed and updated.\n`;
  atomicWrite(profilePath, profileContent);
  atomicWrite(adrPath, adrContent);
  const relativeProfile = normalizePath(path.relative(resolvedTarget, profilePath));
  return {
    apiVersion: API_VERSION,
    kind: 'HomerTargetInitialization',
    status: 'policy-review-required',
    exitCode: EXIT.POLICY_REVIEW_REQUIRED,
    writes: [normalizePath(path.relative(resolvedTarget, adrPath)), relativeProfile].sort(),
    installedSkills: [],
    followUp: `npx --yes @stewietech/homer-odyssey install --target . --profile ${relativeProfile}`,
  };
}

